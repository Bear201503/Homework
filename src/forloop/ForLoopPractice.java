package forloop;

public class ForLoopPractice {

    public static void main(String[] args) {

        for(int i = 0; i <= 5; i++){
            System.out.println("i equals " + i);
        }

        for (int i = 5; i >=0; i--){
            System.out.println("Going backwards, i equals " + i);
        }

    }
}
